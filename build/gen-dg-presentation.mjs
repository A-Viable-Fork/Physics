// Role: the presentation-type overlay generator (DG read-surface build, Part A). A pure read
//   over the real built kernel, applying the operator-ruled kind-to-type table plus the two
//   overrides (killed, sorry-split), and writing the result as static data.
// Contract: `node build/gen-dg-presentation.mjs` writes corpora/dg/dg-presentation.js and exits
//   0. The written file is pure data, no imports, read only by api/dg-client-adapter.mjs.
// Invariant: reads only; never touches corpora/dg/dg-data.js. The overlay carries no grade of
//   its own, only a type from the closed PRESENTATION_TYPES set plus decorative status/tier
//   fields; every claim in the corpus gets exactly one entry.
"use strict";
import { writeFileSync } from "node:fs";
import { buildKernel } from "./dg-build.mjs";
import { characterizedGaps } from "../vendor/kernel/analysis/characterized-gaps.mjs";

const OUTFILE = "corpora/dg/dg-presentation.js";

// the operator-ruled DG kind to presentation type table (DG read-surface build prompt, Part A).
const KIND_MAP = {
  "axiom": "primitive",
  "standard-result": "primitive",
  "observation": "observation",
  "conjecture-adopted": "assumption",
  "derivation": "transformation", // overridden per claim by the sorry split, below
  "prediction": "prediction",
  "mechanism": "transformation",
  "function": "claim",
  "computation": "comparison",
  "block": "claim",
  "branch-commitment": "assumption",
  "departure": "comparison",
  "comment": "assumption",
};

const built = buildKernel();
const graph = { entries: built.state.entries, links: built.state.links, tables: built.tables };
const gapIdentities = new Set(characterizedGaps(graph).map((g) => g.identity));
const withdrawnIdentities = new Set((built.state.withdrawn_records || []).map((w) => w.claim_identity));
const supersededIdentities = new Set((built.state.supersession_records || []).map((s) => s.superseded_identity));

const overlay = {};
let killedOverrideCount = 0;
let sorryCount = 0;
const typeCounts = {};

for (const c of built.claims) {
  const ref = c.spec.ref;
  const identity = c.rec.identity;
  const kind = c.spec.kind;

  let type;
  if (withdrawnIdentities.has(identity)) {
    // killed override: a withdrawn claim maps to `claim` regardless of its original kind.
    type = "claim";
    killedOverrideCount++;
  } else if (kind === "derivation") {
    // sorry split: a derivation that is a characterized gap is the contested question; a
    // derivation that is not is an ordinary reasoning step.
    if (gapIdentities.has(identity)) { type = "question"; sorryCount++; }
    else type = "transformation";
  } else {
    type = KIND_MAP[kind];
  }
  if (!type) throw new Error(`gen-dg-presentation: no presentation type for kind '${kind}' (ref ${ref})`);

  const status = withdrawnIdentities.has(identity) ? "withdrawn" : supersededIdentities.has(identity) ? "superseded" : "in-force";
  const entry = { type, status };
  if (c.rec.declared_grade === "ungraded") entry.tier = "speculation";

  overlay[ref] = entry;
  typeCounts[type] = (typeCounts[type] || 0) + 1;
}

const totalClaims = built.claims.length;
const mappedCount = Object.keys(overlay).length;
if (mappedCount !== totalClaims) throw new Error(`gen-dg-presentation: mapped ${mappedCount} of ${totalClaims} claims`);

const header = [
  "// Role: the DG presentation-type overlay (DG read-surface build, Part A). A plain ref-to-type",
  "//   map derived from the operator-ruled kind mapping plus the killed and sorry-split overrides,",
  "//   generated by build/gen-dg-presentation.mjs. Read only by api/dg-client-adapter.mjs.",
  "// Contract: exports DG_PRESENTATION = { [ref]: { type, status, tier? } }. type is always one of",
  "//   the closed PRESENTATION_TYPES set. status is one of in-force, withdrawn, superseded (decoration,",
  "//   not a truth field). tier is present only for speculation-tier (ungraded) claims.",
  "// Invariant: pure data, imported by nothing. corpora/dg/dg-data.js is never edited to carry this;",
  "//   the corpus stays as authored, the view derives types from this overlay.",
  "\"use strict\";",
].join("\n");

const body = `const DG_PRESENTATION = ${JSON.stringify(overlay, null, 2)};\n\nmodule.exports = { DG_PRESENTATION };\n`;

writeFileSync(OUTFILE, header + "\n\n" + body);
console.log(`wrote ${OUTFILE}: ${mappedCount} of ${totalClaims} claims mapped.`);
console.log(`type counts: ${JSON.stringify(typeCounts)}`);
console.log(`killed override: ${killedOverrideCount}, sorry split (question): ${sorryCount}`);
